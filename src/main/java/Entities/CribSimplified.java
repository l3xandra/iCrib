package Entities;

public class  CribSimplified {
	

		private String name;
		private String code;
		
		public CribSimplified(String name, String code) {
			this.name = name;
			this.code = code;
		}
		
		public String getName() {
			return name;
		}
		
		public String getCode() {
			return code;
		}
		
		public void setName(String name) {
			this.name = name;
		}
		
	


}
